    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        Categoria categoria;
        boolean validacion = true;
        int codigo;
        String rubro;
        String descripcion;
        double precio;
        int stock;

//        COMPRUEBA QUE TODOS LOS CAMPOS ESTEN LLENOS
        if (jtCodigo.getText().isEmpty()
                || jtDescripcion.getText().isEmpty()
                || jtPrecio.getText().isEmpty()
                || jtStock.getText().isEmpty()
                || jcbRubro.getSelectedItem().toString().equalsIgnoreCase("Elegir")) {
            JOptionPane.showMessageDialog(this, "No puede haber campos vacios");

        } else {

            codigo = Integer.parseInt(jtCodigo.getText());
            rubro = (String) jcbRubro.getSelectedItem().toString();
            descripcion = (String) jtDescripcion.getText();
            precio = Double.parseDouble(jtPrecio.getText());
            stock = Integer.parseInt(jtStock.getText());

            if (rubro.equalsIgnoreCase("comestibles")) {
                categoria = Categoria.COMESTIBLES;
            } else if (rubro.equalsIgnoreCase("limpieza")) {
                categoria = Categoria.LIMPIEZA;
            } else {
                categoria = Categoria.PERFUMERIA;
            }
            for (Producto produc : Menu.listaProductos) {
                if (produc.getCodigo() == codigo) {
                    validacion = false;
                    break;

                } else {
                    validacion = true;
                }
            }

            if (validacion) {

                Producto producto = new Producto(codigo, descripcion, precio, stock, categoria);
                Menu.cargarProductos(producto);

                JOptionPane.showMessageDialog(this, "Se guardo con exito " + descripcion);
                borraCampos();
            } else {
                JOptionPane.showMessageDialog(this, "El codigo" + codigo + " ya esta en uso");
                jtCodigo.setText("");
            }

        }


    }//GEN-LAST:event_jbGuardarActionPerformed

