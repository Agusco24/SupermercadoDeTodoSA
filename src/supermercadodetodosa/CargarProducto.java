    private void cargarComboBox() {
        jcbRubro.addItem(new String("Elegir"));
        jcbRubro.addItem(Categoria.COMESTIBLES.toString());
        jcbRubro.addItem(Categoria.LIMPIEZA.toString());
        jcbRubro.addItem(Categoria.PERFUMERIA.toString());

    }

    private void borraCampos() {
        jtCodigo.setText("");
        jtDescripcion.setText("");
        jtPrecio.setText("");
        jtStock.setText("");
        jcbRubro.setSelectedItem("Elegir");
    }
